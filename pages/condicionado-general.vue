          El CONTRATANTE o el beneficiario, según corresponda, debe presentar la solicitud de cobertura a INTERSEGURO, incluyendo la documentación e información completa señalada en la póliza de seguro para el proceso de liquidación del siniestro. En caso INTERSEGURO requiera aclaraciones o precisiones adicionales respecto de la documentación e información presentada por el CONTRATANTE, deberá solicitarlas dentro de los primeros veinte (20) días
          <span class="ft11">de recibida la documentación completa</span>
        </p>
        <p class="p46 ft11">que indica la póliza.</p>
        <p
          class="p45 ft0"
        >El CONTRATANTE, según corresponda, deberá abstenerse de realizar reconocimientos, desistimientos, compromisos o transacciones, sin previa comunicación de consentimiento de INTERSEGURO.</p>
        <p
          class="p47 ft0"
        >Si participara un Ajustador de Siniestros, éste informará a INTERSEGURO dentro de las 24 horas siguientes a la fecha en que el Contratante ha cumplido con proporcionarle la información y documentación completa solicitada para que cumpla las funciones que por ley le corresponde.</p>
        <p class="p48 ft0">2.5.7 Carga de información y cooperación</p>
        <p
          class="p49 ft0"
        >El deber del CONTRATANTE de proporcionar a INTERSEGURO, y/o al Ajustador de Siniestros, de haberse nombrado uno, toda la información y ayuda necesarias para determinar las causas, circunstancias y responsabilidades del siniestro, se mantiene aun después de haber sido indemnizado.</p>
        <p class="p50 ft0">2.5.8 Incumplimiento del CONTRATANTE</p>
        <p
          class="p49 ft0"
        >El incumplimiento de las disposiciones del presente capítulo por dolo o culpa inexcusable del CONTRATANTE liberará automáticamente a INTERSEGURO de su obligación de pago por cualquier siniestro, sin perjuicio de su derecho de resolución del contrato de seguro.</p>
        <p class="p51 ft0">2.6.- Nulidad del contrato de seguro 2.6.1 Causales</p>
        <p
          class="p52 ft0"
        >La Póliza, es nula de pleno derecho, es decir sin efecto legal alguno, en los siguientes casos:</p>
        <p class="p53 ft0">
          <span class="ft5">a)</span>
          <span class="ft13">Ausencia de interés asegurable.</span>
        </p>
        <p class="p54 ft0">
          <span class="ft0">b)</span>
          <span
            class="ft13"
          >Inexistencia o desaparición del riesgo al momento de la celebración del contrato de seguro o producción del siniestro antes de dicha celebración. Reticencia y/o declaración inexacta dolosa o con culpa inexcusable, de circunstancias conocidas por el CONTRATANTE que hubiese impedido el contrato o modificado sus condiciones si INTERSEGURO hubiese sido informada del verdadero estado del riesgo. INTERSEGURO tiene un plazo de treinta (30) días para invocar la nulidad por reticencia y/o declaración inexacta, computado desde que INTERSEGURO la conoció. El pronunciamiento de INTERSEGURO debe ser notificado por medio fehaciente. INTERSEGURO debe probar la reticencia y/o declaración inexacta mediante los medios de prueba consagrados en el ordenamiento jurídico. Si el siniestro se produce antes del vencimiento del plazo que tiene INTERSEGURO para invocar la nulidad, ésta se encuentra liberada del pago del siniestro.</span>
        </p>
        <p class="p55 ft0">
          <span class="ft5">c)</span>
          <span class="ft15">Sobreseguro de mala fe.</span>
        </p>
        <p
          class="p56 ft0"
        >Si hubo intención del CONTRATANTE de enriquecerse a costa de INTERSEGURO, y al tiempo del siniestro el valor asegurado excede el valor asegurable.</p>
        <p class="p50 ft0">2.6.2 Efectos sobre la prima</p>
        <p
          class="p57 ft0"
        >En caso de reticencia y/o declaración inexacta dolosa, las primas pagadas son de propiedad de INTERSEGURO, quien tiene derecho al cobro de las acordada para el</p>
        <p
          class="p25 ft0"
        >primer año de duración del contrato, a título indemnizatorio. En los demás literales del numeral 2.6.1, se reembolsará la prima, sin perjuicio de la acción judicial para lograr el resarcimiento de los daños y perjuicios causados.</p>
        <p class="p22 ft0">2.6.3 Pérdida de derechos indemnizatorios</p>
        <p
          class="p25 ft0"
        >Desde que el CONTRATANTE incurre en causal de nulidad, devienen en ineficaces todos los derechos y beneficios pactados en la Póliza a su favor y caducan automáticamente los que pudieran haberse generado.</p>
        <p
          class="p27 ft0"
        >Si el CONTRATANTE ya hubiera cobrado la indemnización por algún siniestro o gozado de algún otro beneficio emanado de la Póliza, deberá devolverlo conjuntamente con los intereses legales y tributos que se hubiesen devengado o generado de ser el caso.</p>
        <p class="p16 ft0">2.7.- Resolución del contrato de seguro</p>
        <p
          class="p12 ft0"
        >El contrato puede ser resuelto sin expresión de causa por cualquiera de las partes antes del vencimiento del plazo estipulado en la póliza, debiendo comunicar tal decisión a su contraparte con antelación no menor a treinta (30) días calendario, a través de los mismos medios en que se llevó a cabo la contratación. Si INTERSEGURO es quien resuelve el contrato, reembolsará la prima correspondiente al plazo no corrido.</p>
        <p
          class="p11 ft0"
        >Si el CONTRATANTE es quien resuelve, INTERSEGURO tendrá derecho a la prima devengada por el tiempo transcurrido.</p>
        <p class="p14 ft0">El contrato además, puede resolverse por las siguientes causales:</p>
        <p class="p14 ft0">
          <span class="ft5">a)</span>
          <span class="ft16">Por falta de pago de la prima</span>
        </p>
        <p
          class="p31 ft0"
        >Cuando la cobertura esté suspendida por incumplimiento de pago de primas, INTERSEGURO puede resolver el contrato sin ser responsable por los siniestros ocurridos en tales circunstancias, conforme al procedimiento descrito en el numeral 2.4.4 del Capítulo II del presente documento.</p>
        <p class="p58 ft0">
          <span class="ft0">b)</span>
          <span class="ft17">Por solicitud de cobertura fraudulenta o engañosa</span>
        </p>
        <p
          class="p31 ft0"
        >El contrato de seguro quedará resuelto, perdiendo el CONTRATANTE todo derecho emanado de la póliza, si se ha efectuado una solicitud de cobertura fraudulenta o apoyada en documentos o declaraciones falsas. INTERSEGURO tiene derecho a percibir la prima proporcional al tiempo transcurrido de vigencia de la cobertura.</p>
        <p class="p16 ft0">
          <span class="ft5">c)</span>
          <span class="ft18">Por siniestro causado por acto u omisión intencional</span>
        </p>
        <p
          class="p59 ft0"
        >El contrato de seguro quedará resuelto, perdiendo el CONTRATANTE todo derecho emanado de la Póliza, si el siniestro fuera causado por un acto y/u omisión intencional proveniente de dolo o culpa inexcusable del CONTRATANTE. INTERSEGURO tiene derecho a percibir la prima proporcional al tiempo transcurrido.</p>
        <p class="p16 ft0">
          <span class="ft0">d)</span>
          <span class="ft17">Por aumento del riesgo</span>
        </p>
        <p
          class="p31 ft0"
        >El contrato de seguro quedará resuelto, perdiendo el CONTRATANTE todo derecho emanado de la Póliza, por una agravación del riesgo no declarada por escrito oportunamente. INTERSEGURO tiene derecho a percibir la prima proporcional al tiempo transcurrido. En caso se hubiera realizado el pago total de la prima, se procederá con la devolución de la prima por el periodo no corrido. El CONTRATANTE debe notificar a INTERSEGURO los hechos o circunstancias que agraven el riesgo y sean de tal magnitud que, si son conocidas por éste al momento de perfeccionarse el</p>
        <p
          class="p25 ft0"
        >contrato, no lo celebraría o lo haría en condiciones más gravosas. Comunicada a INTERSEGURO la agravación del estado del riesgo, éste debe manifestar al CONTRATANTE, en el plazo de quince (15) días, su voluntad de mantener las condiciones del contrato, modificarlas o resolverlas. Mientras INTERSEGURO no manifieste su posición frente a la agravación, continúan vigentes las condiciones del contrato original. El derecho de INTERSEGURO a resolver caduca si no se ejerce en el plazo previsto o si la agravación del riesgo ha desaparecido.</p>
        <p
          class="p60 ft11"
        >d.1) Efectos en caso de siniestros: Si el CONTRATANTE no informa el aumento del riesgo y ocurre un siniestro, INTERSEGURO es liberada de su obligación excepto que:</p>
        <p class="p61 ft0">
          <span class="ft2">-</span>
          <span class="ft19">El CONTRATANTE no informe a INTERSEGURO sin culpa inexcusable;</span>
        </p>
        <p class="p62 ft0">
          <span class="ft8">-</span>
          <span
            class="ft20"
          >Si la agravación del riesgo no influye en la ocurrencia del siniestro ni sobre el monto indemnizable.</span>
        </p>
        <p class="p63 ft0">
          <span class="ft8">-</span>
          <span
            class="ft20"
          >INTERSEGURO haya tomado conocimiento de la agravación del riesgo producto de la evaluación detallada en el numeral 2.5.3 y no haya ejercido el derecho a resolver o proponer la modificación del contrato en el plazo previsto en el literal d) del Artículo 2.7;</span>
        </p>
        <p class="p64 ft0">
          <span class="ft2">-</span>
          <span
            class="ft19"
          >INTERSEGURO conozca la agravación al tiempo en que debía hacerse la denuncia.</span>
        </p>
        <p
          class="p27 ft0"
        >En los supuestos mencionados en los tres primeros puntos, INTERSEGURO podrá descontar de la indemnización el monto adicional al que hubiese ascendido la prima, de haber sido informado de la agravación del riesgo contratado.</p>
        <p
          class="p65 ft0"
        >d.2) Excepciones a la agravación del riesgo: Las disposiciones sobre agravación del riesgo no se aplican cuando se provoque para evitar el siniestro o para atenuar sus consecuencias, por un deber de humanidad generalmente aceptado, por legítima defensa, por estado de necesidad o por cumplimiento de un deber legal.</p>
        <p class="p66 ft0">
          <span class="ft5">e)</span>
          <span class="ft16">Por ocultamiento intencional de información</span>
        </p>
        <p
          class="p25 ft0"
        >El contrato de seguro quedará resuelto, perdiendo el CONTRATANTE todo derecho emanado de la Póliza, por su ocultamiento intencional de información necesaria para evaluar y valorizar los daños, así como de las causas y consecuencias del siniestro. INTERSEGURO tiene derecho a percibir la prima proporcional al tiempo transcurrido. Se entenderá resuelto el contrato de seguro cuando el CONTRATANTE sea notificado con la comunicación de resolución, a través de los MECANISMOS ALTERNATIVOS y dentro de los plazos de atención de la solicitud de cobertura.</p>
        <p class="p58 ft0">
          <span class="ft5">f)</span>
          <span class="ft21">Por reticencia y/o declaración inexacta no dolosa</span>
        </p>
        <p
          class="p31 ft0"
        >Si la reticencia y/o declaración inexacta no obedece a dolo o culpa inexcusable del CONTRATANTE y es verificada antes de que ocurra el siniestro, INTERSEGURO debe ofrecerle la revisión del contrato en un plazo de treinta (30) días desde la verificación. La propuesta debe contener un ajuste de primas y/o en la cobertura. Asimismo, deberá otorgar un plazo de diez (10) días para que el CONTRATANTE</p>
        <p
          class="p67 ft0"
        >acepte o rechace. Si es aceptada, el reajuste de la prima se paga según lo acordado. A</p>
        <p
          class="p25 ft0"
        >falta de aceptación, INTERSEGURO puede resolver el contrato en comunicación dirigida al CONTRATANTE dentro de treinta (30) días desde el vencimiento del plazo de diez (10) días fijado en el párrafo anterior. Corresponde a INTERSEGURO las primas devengadas a prorrata, hasta el momento en que efectuó la resolución.</p>
        <p class="p14 ft0">
          <span class="ft0">g)</span>
          <span class="ft22">Por obstaculizar el ejercicio de los derechos de INTERSEGURO</span>
        </p>
        <p
          class="p31 ft0"
        >Si el CONTRATANTE o quien actúe en su representación, con su conocimiento, no permite a INTERSEGURO acceder a información o documentación necesaria para evaluar el siniestro u obstaculiza la investigación, el contrato de seguro quedará resuelto, perdiendo el CONTRATANTE todo derecho emanado de la Póliza. Se entenderá resuelto el contrato de seguro cuando el CONTRATANTE sea notificado con la comunicación de resolución, a través de los MECANISMOS ALTERNATIVOS y dentro de los plazos de atención de la solicitud de cobertura.</p>
        <p class="p16 ft0">
          <span class="ft0">h)</span>
          <span class="ft17">Luego de producido el siniestro</span>
        </p>
        <p
          class="p31 ft0"
        >INTERSEGURO o El CONTRATANTE podrán resolver el contrato luego de pagada la indemnización o prestación ofrecida. Una vez resuelto el contrato, se verificará si desde la fecha de resolución en adelante, hay prima pagada en exceso.</p>
        <p class="p16 ft0">
          <span class="ft5">i)</span>
          <span class="ft23">Derecho de arrepentimiento</span>
        </p>
        <p
          class="p25 ft0"
        >El CONTRATANTE tendrá derecho a resolver el contrato, sin expresión de causa ni penalidad alguna, dentro de los quince (15) días calendario siguientes de haber recibido la póliza de seguro, en cuyo caso se le devolverá el total de la prima que hubiese pagado en el plazo de treinta (30) días calendario contados desde la recepción de la solicitud, no sujeta a penalidad. Este derecho se ejerce solicitando la Resolución de la Póliza y se podrá emplear los mismos mecanismos, lugar, medios, o los MECANISMOS ALTERNATIVOS por los cuales se contrató, de acuerdo a lo señalado en las normas sobre protección al consumidor.</p>
        <p
          class="p68 ft0"
        >En los supuestos mencionados en el primer párrafo y en los literales b), c), e), f), g) y h), de existir prima pagada en exceso, INTERSEGURO procederá a su devolución al CONTRATANTE dentro de los treinta (30) días calendario siguientes a la fecha de resolución. En todos los supuestos INTERSEGURO comunicará al CONTRATANTE la Resolución de la Póliza, a través de los MECANISMOS ALTERNATIVOS.</p>
        <p class="p69 ft0">2.8.- Atención de siniestros</p>
        <p class="p22 ft0">2.8.1 Participación del ajustador o perito</p>
        <p
          class="p31 ft0"
        >El Ajustador de Siniestros, contará con un plazo de veinte (20) días para emitir y presentar a INTERSEGURO el informe que sustente la cobertura y liquidación del siniestro, o, en caso contrario el rechazo del siniestro, contado a partir de la fecha en que recibió la documentación e información completa exigida en la póliza de seguro para el proceso de liquidación del siniestro. Si el ajustador de siniestros requiere aclaraciones o precisiones adicionales respecto de la documentación e información presentada, debe solicitarlas al CONTRATANTE o al beneficiario antes del vencimiento del referido plazo; lo que suspenderá el plazo hasta que se presente la documentación e información correspondiente. El ajustador de siniestros deberá informar a INTERSEGURO, dentro de las veinticuatro (24) horas siguientes, cuando se haya completado la documentación e información requerida para el proceso de liquidación del siniestro</p>
        <p
          class="p70 ft2"
        >La opinión del ajustador no obliga a las partes y es independiente de ellas. Los informes del ajustador deben ser proporcionados simultáneamente a ambas partes. Si cualquiera de las partes no esté de acuerdo con los informes, las partes por acuerdo podrán designar a otro ajustador para un nuevo ajuste, designación que deberá producirse dentro del plazo de treinta (30) días calendarios de emitido el informe o informes del ajustador original; de no llegar a acuerdo o no optar por una nueva designación de ajustador, cualquiera de las partes podrá recurrir al medio de solución de controversias que corresponde. Los costos de la designación de un nuevo</p>
        <p
          class="p71 ft2"
        >ajustador son de cargo de la parte que no se encuentra de acuerdo con los informes del ajustador originalmente designado.</p>
        <p
          class="p72 ft2"
        >Los ajustadores a designarse deben estar inscritos en el registro correspondiente que administra la Superintendencia de Banca, Seguros y AFP.</p>
        <p class="p14 ft0">2.8.2 Modalidades de indemnización</p>
        <p
          class="p73 ft2"
        >INTERSEGURO indemnizará las pérdidas ocasionadas por el siniestro, a su libre elección;</p>
        <p class="p74 ft8">
          <span class="ft24">a)</span>
          <span
            class="ft25"
          >reembolsando, en el importe que corresponda según los términos y condiciones de la Póliza, los gastos incurridos por el CONTRATANTE;</span>
        </p>
        <p class="p75 ft2">
          <span class="ft24">b)</span>
          <span
            class="ft26"
          >pagando el monto correspondiente hasta el límite de la suma asegurada, según la magnitud del daño y/o pérdida;</span>
        </p>
        <p class="p24 ft8">
          <span class="ft24">c)</span>
          <span class="ft27">Disponiendo la reparación de los daños ocasionados por el siniestro;</span>
        </p>
        <p class="p74 ft2">
          <span class="ft24">d)</span>
          <span
            class="ft28"
          >reponiendo el vehículo asegurado por otro de similares características, siempre dentro de los límites de la suma asegurada nominalmente expresada.</span>
        </p>
        <p class="p14 ft0">2.8.3 Ganancia total de la prima en caso de pérdida total</p>
        <p
          class="p17 ft2"
        >En caso de pérdida total se generará automáticamente el derecho de INTERSEGURO de cobro de la totalidad de la prima pactada, pudiendo descontarla de la indemnización del siniestro, incluyendo las cuotas insolutas, estén vencidas o no. Podrá también deducir todo adeudo que tuviera el CONTRATANTE con INTERSEGURO correspondiente a la misma Póliza.</p>
        <p class="p16 ft0">2.8.4 Consentimiento del siniestro</p>
        <p
          class="p76 ft0"
        >Queda consentido el siniestro cuando INTERSEGURO aprueba o no rechaza el convenio de ajuste debidamente firmado por el CONTRATANTE en un plazo no mayor de diez (10) días calendario contados desde su suscripción y notificación a</p>
        <p class="p73 ft0">INTERSEGURO.</p>
        <p
          class="p12 ft0"
        >Si INTERSEGURO no esté de acuerdo con el ajuste señalado en el convenio, puede exigir un nuevo ajuste en un plazo no mayor de treinta (30) días calendario, para consentir o rechazar el siniestro, determinar un nuevo monto o proponer acudir a la cláusula de arbitraje o a la vía judicial.</p>
        <p
          class="p77 ft0"
        >Si el ajustador requiere contar con un plazo adicional para concluir el proceso de liquidación del siniestro podrá solicitar a la Superintendencia, por única vez, una prórroga debidamente fundamentada precisando las razones técnicas y el plazo requerido, sujetándose al procedimiento correspondiente establecido en el Texto Unico de Procedimientos Administrativos de la Superintendencia. Esta solicitud suspende el plazo con que cuenta el ajustador de siniestros para emitir el informe correspondiente, hasta que la Superintendencia emita pronunciamiento y éste le sea comunicado. La presentación de la solicitud de prórroga efectuada a la Superintendencia debe comunicarse a INTERSEGURO y al CONTRATANTE dentro de los tres (3) días siguientes de iniciado el procedimiento administrativo. Asimismo, el pronunciamiento de la Superintendencia deberá comunicarse al CONTRATANTE y a INTERSEGURO en el mismo plazo antes señalado, contado a partir de la fecha en que el solicitante haya tomado conocimiento del pronunciamiento correspondiente.</p>
        <p
          class="p78 ft0"
        >Si objetivamente no existe convenio de ajuste por no haberse requerido participación del ajustador o éste aún no ha concluido su informe, el siniestro quedará consentido cuando INTERSEGURO no se haya pronunciado sobre el monto reclamado en un plazo de treinta (30) días calendario contados desde la fecha de haberse completado toda la documentación exigida en la póliza para el pago del siniestro.</p>
        <p class="p24 ft0">2.8.5 Ampliación de la investigación y reserva de investigación posterior</p>
        <p
          class="p25 ft8"
        >Si INTERSEGURO requiere contar con un plazo mayor para investigaciones adicionales u obtener evidencias suficientes sobre la procedencia del siniestro o para la adecuada determinación de su monto y el CONTRATANTE no apruebe la ampliación de dicho plazo, podrá, dentro de los treinta</p>
        <p class="p79 ft2">
          <span class="ft24">(30)</span>
          <span
            class="ft26"
          >días que tiene de plazo para dar por consentido el siniestro o rechazarlo, solicitar una ampliación a la Superintendencia de Banca, Seguros y AFP por única vez, con la debida justificación y por un plazo no mayor al original.</span>
        </p>
        <p
          class="p27 ft2"
        >INTERSEGURO se reserva el derecho de investigar las causas reales del siniestro, aun cuando hubiere pagado la indemnización. Si el CONTRATANTE no cooperara o si de la investigación resultara que el siniestro no estaba cubierto, perderá automáticamente todo derecho emanado de la Póliza, quedando obligado a la restitución de las prestaciones o sumas recibidas de INTERSEGURO, más los intereses legales y gastos incurridos. Si se hubiere pagado en exceso, el CONTRATANTE deberá restituir la prestación o suma recibida en exceso, más los intereses legales correspondientes.</p>
        <p class="p29 ft0">2.8.6 Gastos con cargo a la póliza</p>
        <p
          class="p25 ft2"
        >Sin la autorización previa y escrita de INTERSEGURO, el CONTRATANTE no podrá asumir compromisos o incurrir en gastos con cargo a la Póliza, sea arbitral, judicial o extrajudicial, ni reconocer, conciliar, pagar parcial o totalmente ni transigir un siniestro.</p>
        <p class="p32 ft0">2.8.7 Concurrencia de seguros y concurrencia de coberturas</p>
        <p
          class="p36 ft2"
        >Cuando ocurra un siniestro cubierto por la presente póliza y exista(n) otro(s) seguro(s) sobre el vehículo, INTERSEGURO sólo estará obligada a pagar los daños y pérdidas proporcionalmente a la suma asegurada por INTERSEGURO.</p>
        <p
          class="p27 ft2"
        >En caso de concurrencia de seguros, solo mediante expreso y previo acuerdo indicado en las condiciones particulares, esta Póliza actuará bajo las condiciones del seguro más antiguo.</p>
        <p
          class="p25 ft2"
        >En caso de concurrencia de coberturas de una o varias Pólizas emitidas por INTERSEGURO, se activará primero la específica a la naturaleza del siniestro. En caso exista un exceso de la pérdida cubierta, se activan las coberturas restantes. Los siniestros que se produzcan serán atendidos con arreglo a las normas que regulan la cobertura principal.</p>
        <p class="p16 ft0">
          <NOBR>2.9.-Cambio</NOBR>del CONTRATANTE y/o BENEFICIARIO
        </p>
        <p class="p29 ft0">2.9.1 Cesión del bien o interés asegurado</p>
        <p
          class="p36 ft2"
        >Si el vehículo asegurado es transferido a un tercero, termina el contrato de seguro y toda responsabilidad de INTERSEGURO, al décimo día siguiente de la transferencia, salvo que el CONTRATANTE ceda también el contrato de seguro al tercero con la aprobación del asegurador.</p>
        <p class="p22 ft0">2.9.2 Titular de la Póliza</p>
        <p
          class="p31 ft2"
        >Ninguna de las disposiciones de la presente Póliza otorgará derecho frente a INTERSEGURO a otra persona que no sea el propio CONTRATANTE, sus herederos o el ENDOSATARIO.</p>
        <p class="p14 ft0">2.10.- Reducción de la suma asegurada</p>
        <p class="p22 ft0">2.10.1 Suma asegurada después del siniestro</p>
